class Java 
{
	public static void main(String[] args) 
	{
		byte b = 29;
		short s = 40;
		long l = 200;
		int i = 10;
		float j = 20.0F;
		double f = 40.0d;
		char c1 = '%';
		char c2 = 'a';
		boolean b1 = true;
		boolean b2 = false;
		System.out.println("Int value is :" + i + 10);
		System.out.println("Float value is :" +j);
		System.out.println("double value is :" +f);
		System.out.println("char value is :" +c1);
		System.out.println("char value is :" +c2);
		System.out.println("boolean value is :" +b1);
		System.out.println("boolean value is :" +b2);
		System.out.println("byte value is :" +b);
		System.out.println("short value is :" +s);
		System.out.println("long value is :" +l);
	}
}
